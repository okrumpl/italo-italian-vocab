// Pomocná funkce pro převod čísla na italský název pro doplnění databáze
function getItalianNumberName(n) {
  const units = ["", "uno", "due", "tre", "quattro", "cinque", "sei", "sette", "otto", "nove"];
  const teens = ["dieci", "undici", "dodici", "tredici", "quattordici", "quindici", "sedici", "diciassette", "diciotto", "diciannove"];
  const tens = ["", "dieci", "venti", "trenta", "quaranta", "cinquanta", "sessanta", "settanta", "ottanta", "novanta"];
  
  if (n === 100) return "cento";
  if (n < 100) {
    if (n < 10) return units[n];
    if (n >= 10 && n < 20) return teens[n - 10];
    const ten = Math.floor(n / 10);
    const unit = n % 10;
    // venti + uno -> ventuno, venti + otto -> ventotto
    if (unit === 1 || unit === 8) {
      return tens[ten].slice(0, -1) + units[unit];
    }
    return tens[ten] + units[unit];
  }
  
  const hundred = Math.floor(n / 100);
  const remainder = n % 100;
  const hundredStr = hundred === 1 ? "cento" : units[hundred] + "cento";
  
  if (remainder === 0) return hundredStr;
  
  // Ošetření kolize cento + uno -> centuno, cento + otto -> centotto
  const remStr = getItalianNumberName(remainder);
  if (remStr.startsWith("uno") || remStr.startsWith("otto")) {
    return hundredStr + remStr;
  }
  return hundredStr + remStr;
}
